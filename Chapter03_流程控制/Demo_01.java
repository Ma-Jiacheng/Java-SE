package Chapter03_流程控制;

//顺序结构

/*流程控制概述：
    在一个程序的执行过程中，各条语句的执行顺序是对程序结果有直接影响的，所以必须弄清楚每条语句的执行流程，通常通过控制语句来实现
 */
public class Demo_01 {
    public static void main(String[] args) {
        //顺序结构——从头开始逐行执行语句
        System.out.println("第一条语句");
        System.out.println("第二条语句");
        System.out.println("第三条语句");
        System.out.println("第四条语句");
        System.out.println("第五条语句");
    }
}
